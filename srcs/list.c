#include "../fdf.h"

int		createRGB(t_coord *pt)
{
	if (pt->r < 0)
		pt->r = 255;
	if (pt->g < 0)
		pt->g = 255;
	if (pt->b < 0)
		pt->b = 255;
    return ((pt->r & 0xff) << 16) + ((pt->g & 0xff) << 8) + (pt->b & 0xff);
}
